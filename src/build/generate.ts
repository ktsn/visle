import path from 'node:path'

import { normalizePath } from 'vite'

export const clientVirtualEntryId = '\0@visle/client-entry'

export const serverVirtualEntryId = '\0@visle/server-entry'

export const componentWrapPrefix = '\0visle:wrap:'

export function generateClientVirtualEntryCode(componentIds: string[]): string {
  return (
    componentIds
      // Export each component to avoid being tree-shaken
      .map((id, i) => `export { default as _${i} } from '${id}'`)
      .join('\n')
  )
}

export function generateServerVirtualEntryCode(entryDir: string, componentIds: string[]): string {
  const imports = componentIds.map((id, i) => `import _${i} from '${id}'`).join('\n')

  const entries = componentIds
    .map((id, i) => {
      const key = normalizePath(path.relative(entryDir, id).replace(/\.vue$/, ''))
      return `  '${key}': _${i}`
    })
    .join(',\n')

  return `${imports}\nexport default {\n${entries}\n}`
}

export function generateComponentWrapperCode(
  filePath: string,
  componentRelativePath: string,
  customElementEntryRelativePath: string,
  importedNames: string[],
): string {
  const normalizedFilePath = normalizePath(filePath)
  const normalizedRelativePath = normalizePath(componentRelativePath)
  const normalizedEntryRelativePath = normalizePath(customElementEntryRelativePath)

  const serializedFilePath = JSON.stringify(normalizedFilePath)
  const serializedRelativePath = JSON.stringify(normalizedRelativePath)
  const serializedEntryRelativePath = JSON.stringify(normalizedEntryRelativePath)

  const specifiers = importedNames.map((name, i) => `${name} as __visle_${i}`).join(', ')
  const imports = `import { ${specifiers} } from ${serializedFilePath}`

  const exports = importedNames
    .map((name, i) =>
      name === 'default'
        ? `export default createComponentWrapper(${serializedRelativePath}, ${serializedEntryRelativePath}, ${JSON.stringify(name)}, __visle_${i})`
        : `export const ${name} = createComponentWrapper(${serializedRelativePath}, ${serializedEntryRelativePath}, ${JSON.stringify(name)}, __visle_${i})`,
    )
    .join('\n')

  return `import { createComponentWrapper } from 'visle/internal'
export * from ${serializedFilePath}
${imports}
${exports}
`
}

export function generateEntryTypesCode(
  entryDir: string,
  root: string,
  componentIds: string[],
): string {
  const entries = componentIds
    .map((id) => {
      const key = normalizePath(path.relative(entryDir, id).replace(/\.vue$/, ''))
      const importPath = normalizePath(path.relative(root, id))
      return `    '${key}': ComponentProps<typeof import('./${importPath}')['default']>`
    })
    .join('\n')

  return `/* eslint-disable */
// @ts-nocheck
// Generated by Visle. !! DO NOT MODIFY THIS FILE !!
// Make sure to add this file to your tsconfig.json file as an "include" or "files" entry.

declare module 'visle' {
  import type { ComponentProps } from 'visle'

  interface VisleEntries {
${entries}
  }
}
export {}
`
}
